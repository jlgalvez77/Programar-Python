print(10 > 2 and 9 > 100)   # Con el operador and solo da verdadero solo si las dos condiciones son verdaderas
print(88 <= 110 or 60 > 110)    # Con el operador or, se evalua a verdadero si uno de sus valores es verdadero
print(not 100 == 100)   # El operador not, niega el valor, si es True lo convierte a False y un False lo convierte en True
