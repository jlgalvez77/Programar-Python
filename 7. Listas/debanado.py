lista = [10, 20, 3.14, 'José', 'Gálvez', 7, 'Estudiante', 'cuaderno']
print(lista)

print(lista[0:5])   # Va desde el elemento 0 hasta el 4
print(lista[:2])    # Va desde el inicio de la lista hasta el elemento 1
print(lista[1:])    # Va desde el elemento 1 hasta el final
print(lista[-1:])   # Muestra el ultimo elemento de la lista
print(lista[::-1])  # Muestra la lista invirtiendo el orden