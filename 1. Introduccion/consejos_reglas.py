import keyword
# Este es un ejemplo de código Python que imprime las palabras reservadas del lenguaje.
# Las palabras reservadas son aquellas que tienen un significado especial en Python y no se pueden usar
keywords = keyword.kwlist
print("Palabras reservadas en Python:")
for kw in keywords:
    print(kw)

