cadena = 'Estoy utilizando los metodos de Python'

print(cadena.lower())   # Convierte todos los caracteres de la cadena a minusculas
print(cadena.upper())   # Convierte todos los caracteres de la cadena a mayusculas
print(cadena.title())   # Convierte la primera letra de cada palabra en mayusculas
print(cadena.capitalize())  # Muestra la primera letra de la cadena en mayuscula
print(cadena.swapcase())    # Intercambia las mayusculas por minusculas y las minusculas por mayusculas
