cadena = 'Estoy mostrando los metodos booleanos para las Strings'

print(cadena.startswith('E')) # Devuelve True si la cadena empieza con el valor proporcionado
print(cadena.endswith('e')) # Devuelve True si la cadena acaba con el valor dado
print(cadena.isupper()) # Devuelve True si toda la cadena está en mayusculas
print(cadena.islower()) # Devuelve True si toda la cadena está en minusculas
