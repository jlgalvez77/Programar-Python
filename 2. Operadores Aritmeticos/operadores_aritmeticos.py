# Suma
print(5 + 3)  # Suma de dos números
# Resta
print(10 - 2)  # Resta de dos números
# Multiplicación
print(4 * 7)  # Multiplicación de dos números
# División
print(20 / 3)  # División de dos números
# División entera
print(20 // 3)  # División entera de dos números
# Módulo (resto de la división)
print(20 % 3)  # Módulo de dos números
# Potencia
print(2 ** 3)  # Potencia de dos números (2 elevado al cubo)
# Raíz cuadrada (usando el operador de potencia)
import math
print(16 ** 0.5)  # Raíz cuadrada de 16 (equivalente a 16 ** (1/2))
# Raíz cuadrada (usando la función sqrt de la biblioteca math)
print(math.sqrt(16))  # Raíz cuadrada de 16 usando math.sqrt

