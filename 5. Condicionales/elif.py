letra = ''

if letra.lower() == 'a':
    print('La vocal es la A')
elif letra.lower() == 'e':
    print('La vocal es la E')
elif letra.lower() == 'i':
    print('La vocal es la I')
elif letra.lower() == 'o':
    print('La vocal es la O')
elif letra.lower() == 'u':
    print('La vocal es la U')
else:
    print('No es ninguna vocal')